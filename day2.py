# Creating a list with 5 elements
my_list = [10, 20, 30, 40, 50]

# Accessing elements based on index
first_element = my_list[0]  # Accessing the first element
third_element = my_list[2]  # Accessing the third element
last_element = my_list[-1]  # Accessing the last element

print(f"First element: {first_element}")
print(f"Third element: {third_element}")
print(f"Last element: {last_element}")



# Creating a tuple with 5 elements
my_tuple = ('apple', 'banana', 'cherry', 'date', 'elderberry')

# Accessing elements based on index
second_element = my_tuple[1]  # Accessing the second element
fourth_element = my_tuple[3]  # Accessing the fourth element
negative_element = my_tuple[-2]  # Accessing the second-to-last element

print(f"Second element: {second_element}")
print(f"Fourth element: {fourth_element}")
print(f"Second-to-last element: {negative_element}")



# Creating a dictionary with 5 elements
my_dict = {
    'name': 'Alice',
    'age': 30,
    'city': 'New York',
    'job': 'Engineer',
    'country': 'USA'
}

# Accessing elements based on keys
name_value = my_dict['name']  # Accessing the value for 'name'
job_value = my_dict['job']  # Accessing the value for 'job'

print(f"Name: {name_value}")
print(f"Job: {job_value}")
